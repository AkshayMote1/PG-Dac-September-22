class Pattern7{

public static void main(String args[]){
int k=0;
    for(int i=9;i>=1;i--)
	{
		
		
		for(int j=0;j<=k;j++)
	    {
		System.out.print(" ");
		}
		
		
		for(int j=1;j<=i;j++)
	    {
		System.out.print(i+" ");
		}
		
		
		System.out.println();
		k++;		
	}
	
	
}
}